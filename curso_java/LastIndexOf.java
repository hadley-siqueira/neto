public class LastIndexOf {
    public static void main(String[] args) {
        /*
        Esse algoritmo pegará uma string 
        e mostrará o último índice que 
        determinado caractere se encontra, 
        por meio do caractere 
        */ 
        String banana = "banana";
        String ana = "ana";
        System.out.println(banana.lastIndexOf('a'));
        System.out.println(ana.lastIndexOf('a'));
    }
}
