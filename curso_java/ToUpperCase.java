public class ToUpperCase {
    public static void main(String[] args) {
        /*
        Esse algoritmo irá pegar uma string
        e transformará todos os seus caracteres
        minúsculos em caracteres maiúsculos  
        */
        String teste = "Teste";
        String testeMaiusc = teste.toUpperCase();
        System.out.println(testeMaiusc);
    }
}
