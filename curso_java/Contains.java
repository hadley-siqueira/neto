public class Contains {
    public static void main(String[] args) {
        /*
        Esse algoritmo pegará uma string e
        verificará se existe um caractere 
        ou uma string por meio de uma string
        */
        String banana = "banana";
        String ana = "ana";
        System.out.println(banana.contains(ana));
        System.out.println(banana.contains("maça"));
    }
}
