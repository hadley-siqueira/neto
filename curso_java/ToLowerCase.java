public class ToLowerCase {
    public static void main(String[] args) {
        /*
        Esse algoritmo irá pegar uma string 
        e transformará todos os seus caracteres
        maiúsculos em caracteres minúsculos
        */
        String teste = "Teste";
        String testeMinusc = teste.toLowerCase();
        System.out.println(testeMinusc);
    }
}
